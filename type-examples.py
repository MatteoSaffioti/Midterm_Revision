# integer class types - basic whole numbers (5, -6, 0)
print(type(2))
print(type(4*2))
print(type(~3))
print(type(3 | 2))
print(type(3 | 2))
# boolean class types - True or False
print(type(False))
# float class types - real numbers (2.3, 0.4, -5.0)
print(type(2.3))
print(type(4/2))
print(type(3.0*1.0))
# string class types
print(type("xx"))
# NoneType class types
print(type(None)) # the class NoneType shows that a variable or expression does not have a value